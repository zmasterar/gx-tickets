  <h1>Tickets</h1>
Si querés hacer una denuncia por robo, extravío de cuenta o cualquier problema relacionado al juego que necesite la intervención de un GM/Admin, tenés que realizar un ticket en nuestro sistema de soporte:

<center><h2><a href="http://soporte.gxzone.com" target="_blank">Hacé clic aquí para entrar al sistema de TICKETS</a></h2></center>

Esto incluye..

<ul><li>Denuncias</li>
<li>Problemas de logueo (ban?)</li>
<li>Cuentas Robadas</li>
<li>Avisos al staff como por ejemplo bugs criticos encontrados</li>
<li>Problemas con donaciones</li>
<li>etc..</li></ul>

<p>También podés ingresar al sistema de soporte haciendo click en el link SOPORTE en la parte superior de cualquier página de nuestro <a href="http://foros.gxzone.com/forum.php" target="_blank">foro</a> como en la siguiente imagen</p>

